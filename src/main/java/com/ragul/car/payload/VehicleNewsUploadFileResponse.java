package com.ragul.car.payload;

public class VehicleNewsUploadFileResponse {
    private String fileName;
    private String title;
    private String subTitle;
    private String description;
    private String newsDate;
    private String fileDownloadUri;
    private String fileType;
    private long size;

    public VehicleNewsUploadFileResponse(String fileName, String title, String subTitle, String description, String newsDate, String fileDownloadUri, String fileType, long size){
        this.fileName = fileName;
        this.title = title;
        this.subTitle = subTitle;
        this.description = description;
        this.newsDate = newsDate;
        this.fileDownloadUri = fileDownloadUri;
        this.fileType = fileType;
        this.size = size;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public void setSubTitle(String subTitle) {
        this.subTitle = subTitle;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNewsDate() {
        return newsDate;
    }

    public void setNewsDate(String newsDate) {
        this.newsDate = newsDate;
    }

    public String getFileDownloadUri() {
        return fileDownloadUri;
    }

    public void setFileDownloadUri(String fileDownloadUri) {
        this.fileDownloadUri = fileDownloadUri;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }
}
